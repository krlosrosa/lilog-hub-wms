import type {
  DocaItem,
  RecebimentoListaItem,
} from '@/features/recebimento/types/recebimento-lista.schema';

function withHorarioPrevisto(
  item: Omit<RecebimentoListaItem, 'horarioPrevisto'>,
): RecebimentoListaItem {
  const [hours, minutes] = item.horario.split(':').map(Number);
  const date = new Date();
  date.setHours(hours ?? 0, minutes ?? 0, 0, 0);

  return {
    ...item,
    horarioPrevisto: date.toISOString(),
  };
}

