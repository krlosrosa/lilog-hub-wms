function mockHorariosProcessos(etapaAtual: EtapaOperacional): HorariosProcessos {
  const ordem: EtapaOperacional[] = [
    'separacao',
    'conferencia',
    'carregamento',
    'finalizado',
  ];
  const idx = ordem.indexOf(etapaAtual);

  const resolver = (processoIdx: number) => {
    if (idx > processoIdx || etapaAtual === 'finalizado') {
      const base = 18 + processoIdx;
      return { inicio: `${base}:10`, fim: `${base}:45` };
    }

    if (idx === processoIdx) {
      const base = 18 + processoIdx;
      return { inicio: `${base}:10`, fim: null };
    }

    return { inicio: null, fim: null };
  };

  return {
    separacao: resolver(0),
    conferencia: resolver(1),
    carregamento: resolver(2),
  };
}

function mockStatusPorEtapa(etapaAtual: EtapaOperacional): StatusTransporteTorre {
  switch (etapaAtual) {
    case 'separacao':
      return 'EM_SEPARACAO';
    case 'conferencia':
      return 'EM_CONFERENCIA';
    case 'carregamento':
      return 'EM_CARREGAMENTO';
    case 'finalizado':
      return 'CARREGADO';
  }
}

